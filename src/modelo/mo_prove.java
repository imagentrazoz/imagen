package modelo;



public class mo_prove {

    String cod_proveedor, n_empresa;
    int telefono, nit;
    
    public String getCod_proveedor() {
        return cod_proveedor;
    }

    public void setCod_proveedor(String cod_proveedor) {
        this.cod_proveedor = cod_proveedor;
    }

    public String getN_empresa() {
        return n_empresa;
    }

    public void setN_empresa(String n_empresa) {
        this.n_empresa = n_empresa;
    }

    public int getTelefono() {
        return telefono;
    }

    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    public int getNit() {
        return nit;
    }

    public void setNit(int nit) {
        this.nit = nit;
    }

    



}
